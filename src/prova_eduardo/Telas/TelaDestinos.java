    private void salvar() {
        Destino destino = new Destino();
        destino.setCodigo(Integer.parseInt(tfCodigo.getText()));
        destino.setDestino(tfDestino.getText());
        destino.setValorTotal(Double.parseDouble(tfValor.getText()));
        destino.setDataInicial(java.sql.Date.valueOf(tfInicioData.getText()));
        destino.setDataTermino(java.sql.Date.valueOf(tfTerminoData.getText()));

        try {
            if (destinoDAO.getLastId() == Integer.parseInt(tfCodigo.getText())) {
                destinoDAO.save(destino);
            } else {
                destinoDAO.update(destino);
            }
            JOptionPane.showMessageDialog(null, "Registro Salvo Com Sucesso!!!");
            limpaCampos();
            ultimoID();
            btSalvar.setEnabled(false);
            carregaTable(destinoDAO.getAll());
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void filtrar() {
        try {
            if (rbCodigo.isSelected() && tfFiltro.getText().trim().length() != 0) {
                Destino destino = destinoDAO.getById(Integer.parseInt(tfFiltro.getText()));
                List<Destino> destinoList = new ArrayList<>();
                destinoList.add(destino);
                carregaTable(destinoList);
            } else if (rbDestino.isSelected() && tfFiltro.getText().trim().length() != 0) {
                carregaTable(destinoDAO.getByName(tfFiltro.getText()));
            } else {
                carregaTable(destinoDAO.getAll());
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    private void remover() {
        int codigoRemover = (int) Tabela.getValueAt(Tabela.getSelectedRow(), 0);
        try {
            destinoDAO.delete(codigoRemover);
            carregaTable(destinoDAO.getAll());
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

    private void carregaTable(List<Destino> destinoList) {
        if (destinoList == null) {
            return;
        }
        DefaultTableModel model = (DefaultTableModel) Tabela.getModel();
        model.setRowCount(0);
        for (Destino d : destinoList) {
            model.addRow(new Object[]{d.getCodigo(), d.getDestino(), d.getDataInicial(), d.getDataTermino(), d.getValorTotal()});
        }

    }

    private void inicializar() throws SQLException {

        rbCodigo.setSelected(true);

        tfCodigo.setEditable(false);
        tfFiltro.setEditable(true);
        tfDestino.setEditable(false);
        tfValor.setEditable(false);
        tfInicioData.setEditable(false);
        tfTerminoData.setEditable(false);
        ultimoID();
    }

    private void ultimoID() {
        try {
            tfCodigo.setText(String.valueOf(destinoDAO.getLastId()));
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

    private void limpaCampos() {
        tfValor.setText("");
        tfFiltro.setText("");
        tfInicioData.setText("");
        tfTerminoData.setText("");
        tfDestino.setText("");
    }

